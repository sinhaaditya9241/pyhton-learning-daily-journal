import math #module 

number = int(input('enter a number: ')) #input number from user

square_root = math.sqrt(number) #calculate square root of number
natural_log = math.log(number) #calculate natural log of number
sine = math.sin(number) # calculate sine of number

print(f"square root of {number} is {square_root}") #print square root of number
print(f"natural log of {number} is {natural_log}") #print natural log of number
print(f"sine of {number} is {sine}") #print sine of number